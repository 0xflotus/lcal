GREGORIAN_HEADER="Su Mo Tu We Th Fr Sa"
MONTH_NAMES_LONG=(UNUSED January February March April May June July
                  August September October November December)

_center()
{
        printf "%*s\n" $(( (${1} + ${#2}) / 2)) "$2"
}

_print_gregorian_monthly()
{
        _center 21 "${MONTH_NAMES_LONG[m]} $y"

        echo "$GREGORIAN_HEADER"

	for ((i=0; i<126; ((i+=21)) ))
	do
		echo "${CALENDAR_DATA:i:21}"
	done
}

_is_leap()
{
        local y=$1
        if [ $((y % 4)) -eq 0 ] && 
           [ $((y % 100)) -ne 0 ] || 
           [ $((y % 400)) -eq 0 ]; then
                leap=1
        fi
}

_day_of_week()
{
        local m=1 d=1 y=$1

        local mcodes=(UNUSED 6 2 2 5 0 3 5 1 4 6 2 4)
        local w=$(( ((y / 4) - (y / 100)) + (y / 400) + d + y + ${mcodes[m]} ))

        _is_leap $y
        if [ $LEAP ] && [ $m -lt 3 ]; then
                ((w--))
        fi

        return $((w % 7))
}

