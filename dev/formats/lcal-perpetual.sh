GREGORIAN_HEADER="Su Mo Tu We Th Fr Sa"

MONTH_NAMES_LONG=(UNUSED January February March April May June July
                  August September October November December)

_center()
{
        printf "%*s\n" $(( (${1} + ${#2}) / 2)) "$2"
}

# Simply print the calendar.
_print_gregorian_monthly()
{
        _center 21 "${MONTH_NAMES_LONG[m]} $y"

        echo "$GREGORIAN_HEADER"

	for ((i=0; i<126; ((i+=21)) ))
	do
		echo "${CALENDAR_DATA:i:21}"
	done
}

_is_leap()
{
        local y=$1
        if [ $((y % 4)) -eq 0 ] && 
           [ $((y % 100)) -ne 0 ] || 
           [ $((y % 400)) -eq 0 ]; then
                leap=1
        fi
}

# All we need here is the day number of Jan 1st!
_day_of_week()
{
        local m=1 d=1 y=$1

        local mcodes=(UNUSED 6 2 2 5 0 3 5 1 4 6 2 4)
        local w=$(( ((y / 4) - (y / 100)) + (y / 400) + d + y + ${mcodes[m]} ))

        return $((w % 7))
}

_is_year_valid()
{
	case $1 in
		0*|*[a-zA-Z]*)
        	echo "$PROGRAM: year out of range [1753-9999]"
                return 1
	esac

	if [ $1 -lt 1753 ] || [ $1 -gt 9999 ]; then
        	echo "$PROGRAM: year out of range [1753-9999]"
                return 1
	fi
}

_is_month_valid()
{
	if [ $1 -gt 12 ] || [ $1 -lt 1 ]; then
		 echo "$PROGRAM: month out of range [1-12]"
		return 1
	fi
}

_is_date_valid()
{
	local ex

	case $1 in
		*[!0-9]*)
			_parse_month $1
                        ex=$?
                        case $ex in
                        	255)
                                	echo "$PROGRAM: invalid date"
                                        exit 1
                                        ;;
				*)
					shift
					;;
                                esac
				;;
		*)
			ex=$1
			shift
	esac

	for i
	do
		case $i in
			0*|*[!0-9]*)
				echo "$PROGRAM: invalid date"
				exit 1
				;;
		esac
	done

	set -- $ex $1
	if [ $# -eq 0 ]; then
		return 0
	elif [ $# -eq 1 ]; then
		m=$ex
		_is_month_valid $m || exit 1
	elif [ $# -eq 2 ]; then
		m=$ex
		_is_month_valid $m || exit 1
		_is_year_valid $y || exit 1
	fi
}

_parse_month()
{
        declare -l m=$1
        case $m in
                1|jan* )   return 1 ;;
                2|feb* )   return 2 ;;
                3|mar* )   return 3 ;;
                4|apr* )   return 4 ;;
                5|may* )   return 5 ;;
                6|jun* )   return 6 ;;
                7|jul* )   return 7 ;;
                8|aug* )   return 8 ;;
                9|sep* )   return 9 ;;
                10|oct*)   return 10 ;;
                11|nov*)   return 11 ;;
                12|dec*)   return 12 ;;
                      *)   return 255 ;;
        esac
}

